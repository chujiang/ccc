package com.kingnod.etraining.exam.entity;

import java.util.List;

/**
 * 该类主要用于封装搜索试题条件
 * @author PiJingwei
 *
 */
public class ConditionRule {
	
	private List<Integer> rowNumerdList; // 行数
	
	private Float everyQuestionScore; // 每题分数
	
	private Integer examNumber; // 考试次数   --  表示第几次考试
	
	private Long examinationId; // 考试活动ID 
	
	private Long userId; // 考生ID
	
	private Long examPaperId; // 试卷ID
	
	private String[] courseIdArray; // 课程ID
	
	private String[] knowledgeIdArray; // 知识点ID
	
	private List<Long> idsList; // 已经被添加过的试题ID
	
	private List<Long> notInIdsList; // 不在已经被添加过试题ID的范围内
	
	private String conditionType; // 随机类型
	
	private Short degree; // 难度系数
	
	private String[] folderIdArray; // 题库ID

	public String[] getCourseIdArray() {
		return courseIdArray;
	}

	public void setCourseIdArray(String[] courseIdArray) {
		this.courseIdArray = courseIdArray;
	}

	public String[] getKnowledgeIdArray() {
		return knowledgeIdArray;
	}

	public void setKnowledgeIdArray(String[] knowledgeIdArray) {
		this.knowledgeIdArray = knowledgeIdArray;
	}

	public Short getDegree() {
		return degree;
	}

	public void setDegree(Short degree) {
		this.degree = degree;
	}

	public List<Integer> getRowNumerdList() {
		return rowNumerdList;
	}

	public void setRowNumerdList(List<Integer> rowNumerdList) {
		this.rowNumerdList = rowNumerdList;
	}

	public String[] getFolderIdArray() {
		return folderIdArray;
	}

	public void setFolderIdArray(String[] folderIdArray) {
		this.folderIdArray = folderIdArray;
	}

	public Integer getExamNumber() {
		return examNumber;
	}

	public void setExamNumber(Integer examNumber) {
		this.examNumber = examNumber;
	}

	public Long getExaminationId() {
		return examinationId;
	}

	public void setExaminationId(Long examinationId) {
		this.examinationId = examinationId;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public Long getExamPaperId() {
		return examPaperId;
	}

	public void setExamPaperId(Long examPaperId) {
		this.examPaperId = examPaperId;
	}

	public String getConditionType() {
		return conditionType;
	}

	public void setConditionType(String conditionType) {
		this.conditionType = conditionType;
	}

	public Float getEveryQuestionScore() {
		return everyQuestionScore;
	}

	public void setEveryQuestionScore(Float everyQuestionScore) {
		this.everyQuestionScore = everyQuestionScore;
	}

	public List<Long> getIdsList() {
		return idsList;
	}

	public void setIdsList(List<Long> idsList) {
		this.idsList = idsList;
	}

	public List<Long> getNotInIdsList() {
		return notInIdsList;
	}

	public void setNotInIdsList(List<Long> notInIdsList) {
		this.notInIdsList = notInIdsList;
	}
}
