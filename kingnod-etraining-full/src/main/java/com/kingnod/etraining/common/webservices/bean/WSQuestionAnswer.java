package com.kingnod.etraining.common.webservices.bean;

public class WSQuestionAnswer {

	private String type;
	private Long answerIndex;
	private Long itemSeq;
	private String itemContent;
	private Boolean correct;
	private Float score;
	public String getType() {
		return type;
	}
	public Long getAnswerIndex() {
		return answerIndex;
	}
	public Long getItemSeq() {
		return itemSeq;
	}
	public String getItemContent() {
		return itemContent;
	}
	public Boolean getCorrect() {
		return correct;
	}
	public Float getScore() {
		return score;
	}
	public void setType(String type) {
		this.type = type;
	}
	public void setAnswerIndex(Long answerIndex) {
		this.answerIndex = answerIndex;
	}
	public void setItemSeq(Long itemSeq) {
		this.itemSeq = itemSeq;
	}
	public void setItemContent(String itemContent) {
		this.itemContent = itemContent;
	}
	public void setCorrect(Boolean correct) {
		this.correct = correct;
	}
	public void setScore(Float score) {
		this.score = score;
	}
}
