package com.kingnod.etraining.security.entity;

/**
 * 当前权限的值
 * 
 * @author liuxiaobin
 * 
 */
public class OwnerAutValue {
	private int value;//当前权限的值
	private boolean bool;//是否有当前权限
	private boolean inherit; //是否继承的权限
	public OwnerAutValue(){}
	public OwnerAutValue(int value, boolean bool) {
		super();
		this.value = value;
		this.bool = bool;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public boolean isBool() {
		return bool;
	}
	public void setBool(boolean bool) {
		this.bool = bool;
	}
	public boolean isInherit() {
		return inherit;
	}
	public void setInherit(boolean inherit) {
		this.inherit = inherit;
	}
}
